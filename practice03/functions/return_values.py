# W3Schools style examples: return values

def my_function(x):
    return 5 * x

print(my_function(3))
print(my_function(5))
print(my_function(9))


def my_function_return_list():
    return ["apple", "banana", "cherry"]

fruits = my_function_return_list()
print(fruits[0])
print(fruits[1])
print(fruits[2])
